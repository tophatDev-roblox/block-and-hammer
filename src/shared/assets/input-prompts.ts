		export function mouse(button: string | Enum.UserInputType): string | undefined {
			if (typeIs(button, 'string')) {
				switch (button) {
					case 'LMB': {
						return MouseMap.get(Enum.UserInputType.MouseButton1);
					}
					case 'RMB': {
						return MouseMap.get(Enum.UserInputType.MouseButton2);
					}
					case 'MMB': {
						return MouseMap.get(Enum.UserInputType.MouseButton3);
					}
					case 'Blank': {
						return BlankMouse;
					}
					default: {
						return undefined;
					}
				}
			}
			
			return MouseMap.get(button);
		}
