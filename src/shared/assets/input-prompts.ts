		export function keyboard(keyCode: Enum.KeyCode, alt?: boolean): string | undefined {
			const data = KeyboardMap.get(keyCode);
			if (data === undefined) {
				return undefined;
			}
			
			if (typeIs(data, 'string')) {
				return data;
			}
			
			return alt ? data[1] : data[0];
		}
		
